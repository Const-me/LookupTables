	uint64_t testLookup( const alignedVector<uint16_t>& vec )
	{
		const uint8_t* p = (const uint8_t*)vec.data();
		const uint8_t* const pEnd = p + vec.size() * sizeof( uint16_t );

		uint64_t res = BYTE_BIT_COUNTS[ *p ];
		p++;

		for( ; p < pEnd; p++ )
			res += BYTE_BIT_COUNTS[ *p ];
		return res;
	}
